public class Constants {
    public static void main(String[] args) {
//        상수(constants, final variables)
//        한 번 값이 할당 = > 재할당이 불가능
//        전체를 대문자, 단어간 구분은 _
        final double PI = 3.14;
        System.out.println(PI);

//      PI = 3.141592; 는 에러이다 final double PI는 더 이상 할당 받을 수 없기 때문

    }
}
